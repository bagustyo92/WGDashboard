    private String kriteria;
    private String avgVal;
    private String trend;
    private String avgRank;

    private String trendTextColor;

    public String getKriteria() {
        return kriteria;
    }

    public void setKriteria(String kriteria) {
        this.kriteria = kriteria;
    }

    public String getAvgVal() {
        return avgVal;
    }

    public void setAvgVal(String avgVal) {
        this.avgVal = avgVal;
    }

    public String getTrend() {
        return trend;
    }

    public void setTrend(String trend) {
        this.trend = trend;
    }

    public String getAvgRank() {
        return avgRank;
    }

    public void setAvgRank(String avgRank) {
        this.avgRank = avgRank;
    }

    public String getTrendTextColor() {
        return trendTextColor;
    }

    public void setTrendTextColor(String trendTextColor) {
        this.trendTextColor = trendTextColor;
    }
